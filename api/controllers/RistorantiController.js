module.exports = {

    _config: {
        actions: true,
        shortcuts: true,
        rest: true
    },
      // Prove pagine
    baseRests: (req, res) => {
        Ristoranti.find().exec((err, rests) => {
            return res.view('rests', { rests: rests });
        });
    },

    baseRestsRis: (req, res) => {
        Ristoranti.find().exec((err, rests) => {
            return res.view('restris', { restRis: rests });
        });
     }
   };
